import React, { useState } from 'react';
import { useLoyaltyAnalytics, useReviews } from '../hooks/useLoyalty';
import { LoyaltyFilter } from '../types/loyalty';
import { Spinner } from '../components/ui/Spinner';
import { Alert } from '../components/ui/Alert';
import ReviewForm from '../components/ReviewForm';

const Loyalty: React.FC = () => {
  const [activeTab, setActiveTab] = useState<'analytics' | 'reviews' | 'add-review'>('analytics');
  const [filter, setFilter] = useState<LoyaltyFilter>({ period: 'month' });

  const {
    analytics,
    trends,
    summary,
    loading: analyticsLoading,
    error: analyticsError,
    updateFilter: updateAnalyticsFilter,
  } = useLoyaltyAnalytics(filter);

  const {
    reviews,
    loading: reviewsLoading,
    error: reviewsError,
    updateFilter: updateReviewsFilter,
  } = useReviews();

  const handleFilterChange = (newFilter: Partial<LoyaltyFilter>) => {
    const updatedFilter = { ...filter, ...newFilter };
    setFilter(updatedFilter);
    
    if (activeTab === 'analytics') {
      updateAnalyticsFilter(updatedFilter);
    } else {
      updateReviewsFilter(updatedFilter);
    }
  };

  const renderAnalytics = () => {
    if (analyticsLoading) {
      return (
        <div className="flex justify-center py-8">
          <Spinner size="lg" />
        </div>
      );
    }

    if (analyticsError) {
      return <Alert variant="error" message={analyticsError} />;
    }

    return (
      <div className="space-y-6">
        {/* –û–±—â–∞—è —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ */}
        {summary && (
          <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-6 gap-4">
            <div className="bg-white p-6 rounded-lg shadow-md">
              <h3 className="text-sm font-medium text-gray-500">–í—Å–µ–≥–æ –æ—Ç–∑—ã–≤–æ–≤</h3>
              <p className="text-2xl font-bold text-gray-900">{summary.totalReviews}</p>
            </div>
            <div className="bg-white p-6 rounded-lg shadow-md">
              <h3 className="text-sm font-medium text-gray-500">–°—Ä–µ–¥–Ω–∏–π —Ä–µ–π—Ç–∏–Ω–≥</h3>
              <p className="text-2xl font-bold text-blue-600">
                {summary.averageRating.toFixed(1)}/5
              </p>
            </div>
            <div className="bg-white p-6 rounded-lg shadow-md">
              <h3 className="text-sm font-medium text-gray-500">–ê–∫—Ç–∏–≤–Ω—ã—Ö —É—á–∏—Ç–µ–ª–µ–π</h3>
              <p className="text-2xl font-bold text-green-600">{summary.activeTeachers}</p>
            </div>
            <div className="bg-white p-6 rounded-lg shadow-md">
              <h3 className="text-sm font-medium text-gray-500">–ê–∫—Ç–∏–≤–Ω—ã—Ö –≥—Ä—É–ø–ø</h3>
              <p className="text-2xl font-bold text-purple-600">{summary.activeGroups}</p>
            </div>
            <div className="bg-white p-6 rounded-lg shadow-md">
              <h3 className="text-sm font-medium text-gray-500">–ü–æ–≤—Ç–æ—Ä–Ω—ã–µ –ø–æ–∫—É–ø–∫–∏</h3>
              <p className="text-2xl font-bold text-indigo-600">
                {summary.repeatPurchaseRate?.toFixed(1) || 0}%
              </p>
            </div>
            <div className="bg-white p-6 rounded-lg shadow-md">
              <h3 className="text-sm font-medium text-gray-500">–£—Ä–æ–≤–µ–Ω—å —É–¥–æ–≤–ª–µ—Ç–≤–æ—Ä–µ–Ω–Ω–æ—Å—Ç–∏</h3>
              <p className="text-2xl font-bold text-orange-600">
                {summary.satisfactionRate.toFixed(1)}%
              </p>
            </div>
          </div>
        )}

        {/* –†–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Ä–µ–π—Ç–∏–Ω–≥–æ–≤ */}
        {analytics && (
          <div className="bg-white p-6 rounded-lg shadow-md">
            <h3 className="text-lg font-semibold mb-4">–†–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Ä–µ–π—Ç–∏–Ω–≥–æ–≤</h3>
            <div className="space-y-3">
              {analytics.ratingDistribution.map((item) => (
                <div key={item.rating} className="flex items-center">
                  <span className="w-12 text-sm text-gray-600">{item.rating} ‚≠ê</span>
                  <div className="flex-1 bg-gray-200 rounded-full h-3 mx-3">
                    <div
                      className="bg-blue-500 h-3 rounded-full transition-all duration-300"
                      style={{
                        width: `${(item._count.rating / analytics.totalReviews) * 100}%`,
                      }}
                    />
                  </div>
                  <span className="w-16 text-sm text-gray-600 text-right">
                    {item._count.rating} ({((item._count.rating / analytics.totalReviews) * 100).toFixed(1)}%)
                  </span>
                </div>
              ))}
            </div>
          </div>
        )}

        {/* –¢–æ–ø —É—á–∏—Ç–µ–ª–µ–π */}
        {analytics && analytics.topTeachers.length > 0 && (
          <div className="bg-white p-6 rounded-lg shadow-md">
            <h3 className="text-lg font-semibold mb-4">–¢–æ–ø —É—á–∏—Ç–µ–ª–µ–π –ø–æ —Ä–µ–π—Ç–∏–Ω–≥—É</h3>
            <div className="space-y-3">
              {analytics.topTeachers.slice(0, 10).map((teacher, index) => (
                <div key={teacher.teacherId} className="flex items-center justify-between p-3 bg-gray-50 rounded-lg">
                  <div className="flex items-center">
                    <span className="w-8 h-8 bg-blue-100 text-blue-600 rounded-full flex items-center justify-center text-sm font-medium mr-3">
                      {index + 1}
                    </span>
                    <div>
                      <p className="font-medium text-gray-900">
                        {teacher.teacher?.user ? 
                          `${teacher.teacher.user.name} ${teacher.teacher.user.surname}` : 
                          '–ù–µ–∏–∑–≤–µ—Å—Ç–Ω—ã–π —É—á–∏—Ç–µ–ª—å'
                        }
                      </p>
                      <p className="text-sm text-gray-500">
                        {teacher._count.rating} –æ—Ç–∑—ã–≤{teacher._count.rating > 1 ? '–∞' : ''}
                      </p>
                    </div>
                  </div>
                  <div className="text-right">
                    <p className="font-semibold text-blue-600">
                      {teacher._avg.rating?.toFixed(1)}/5
                    </p>
                  </div>
                </div>
              ))}
            </div>
          </div>
        )}

        {/* –¢—Ä–µ–Ω–¥—ã */}
        {trends && trends.length > 0 && (
          <div className="bg-white p-6 rounded-lg shadow-md">
            <h3 className="text-lg font-semibold mb-4">–¢—Ä–µ–Ω–¥—ã —Ä–µ–π—Ç–∏–Ω–≥–æ–≤</h3>
            <div className="space-y-2">
              {trends.slice(-10).map((trend, index) => (
                <div key={index} className="flex items-center justify-between p-2 border-b border-gray-100 last:border-b-0">
                  <span className="text-sm text-gray-600">
                    {new Date(trend.period).toLocaleDateString('ru-RU', {
                      year: 'numeric',
                      month: 'long',
                    })}
                  </span>
                  <div className="flex items-center space-x-4">
                    <span className="text-sm text-gray-600">
                      {trend.review_count} –æ—Ç–∑—ã–≤{trend.review_count > 1 ? '–æ–≤' : ''}
                    </span>
                    <span className="text-sm font-medium text-blue-600">
                      {Number(trend.average_rating).toFixed(1)}/5
                    </span>
                  </div>
                </div>
              ))}
            </div>
          </div>
        )}
      </div>
    );
  };

  const renderReviews = () => {
    if (reviewsLoading) {
      return (
        <div className="flex justify-center py-8">
          <Spinner size="lg" />
        </div>
      );
    }

    if (reviewsError) {
      return <Alert variant="error" message={reviewsError} />;
    }

    if (!reviews || reviews.data.length === 0) {
      return (
        <div className="text-center py-8">
          <p className="text-gray-500">–û—Ç–∑—ã–≤—ã –Ω–µ –Ω–∞–π–¥–µ–Ω—ã</p>
        </div>
      );
    }

    return (
      <div className="space-y-4">
        {reviews.data.map((review) => (
          <div key={review.id} className="bg-white p-6 rounded-lg shadow-md">
            <div className="flex items-start justify-between mb-4">
              <div>
                <h4 className="font-semibold text-gray-900">
                  {review.teacher?.user ? 
                    `${review.teacher.user.name} ${review.teacher.user.surname}` : 
                    '–ù–µ–∏–∑–≤–µ—Å—Ç–Ω—ã–π —É—á–∏—Ç–µ–ª—å'
                  }
                </h4>
                <p className="text-sm text-gray-500">–ì—Ä—É–ø–ø–∞: {review.group?.name}</p>
              </div>
              <div className="flex items-center">
                <div className="flex">
                  {[1, 2, 3, 4, 5].map((star) => (
                    <span
                      key={star}
                      className={`text-lg ${
                        star <= review.rating ? 'text-yellow-400' : 'text-gray-300'
                      }`}
                    >
                      ‚≠ê
                    </span>
                  ))}
                </div>
                <span className="ml-2 text-sm text-gray-600">{review.rating}/5</span>
              </div>
            </div>

            <p className="text-gray-700 mb-4">{review.comment}</p>

            <div className="flex items-center justify-between text-sm text-gray-500">
              <div className="flex items-center space-x-4">
                <span>üëç {review.likes}</span>
                <span>üí° {review.helpful}</span>
              </div>
              <span>
                {new Date(review.createdAt).toLocaleDateString('ru-RU', {
                  year: 'numeric',
                  month: 'long',
                  day: 'numeric',
                })}
              </span>
            </div>
          </div>
        ))}

        {/* –ü–∞–≥–∏–Ω–∞—Ü–∏—è */}
        {reviews.totalPages > 1 && (
          <div className="flex justify-center mt-6">
            <div className="flex space-x-2">
              {Array.from({ length: reviews.totalPages }, (_, i) => i + 1).map((page) => (
                <button
                  key={page}
                  onClick={() => handleFilterChange({ page })}
                  className={`px-3 py-2 rounded-md text-sm font-medium ${
                    page === reviews.page
                      ? 'bg-blue-600 text-white'
                      : 'bg-white text-gray-700 hover:bg-gray-50 border border-gray-300'
                  }`}
                >
                  {page}
                </button>
              ))}
            </div>
          </div>
        )}
      </div>
    );
  };

  return (
    <div className="p-6">
      <div className="mb-6">
        <h1 className="text-2xl font-bold text-gray-900">–ê–Ω–∞–ª–∏–∑ –ª–æ—è–ª—å–Ω–æ—Å—Ç–∏ —Å—Ç—É–¥–µ–Ω—Ç–æ–≤</h1>
        <p className="text-gray-600">–ú–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥ –æ—Ç–∑—ã–≤–æ–≤ –∏ —É–¥–æ–≤–ª–µ—Ç–≤–æ—Ä–µ–Ω–Ω–æ—Å—Ç–∏ —Å—Ç—É–¥–µ–Ω—Ç–æ–≤</p>
      </div>

      {/* –§–∏–ª—å—Ç—Ä—ã */}
      <div className="mb-6 bg-white p-4 rounded-lg shadow-md">
        <div className="flex flex-wrap gap-4">
          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">–ü–µ—Ä–∏–æ–¥</label>
            <select
              value={filter.period || 'month'}
              onChange={(e) => handleFilterChange({ period: e.target.value as any })}
              className="border border-gray-300 rounded-md px-3 py-2 text-sm focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
            >
              <option value="month">–ú–µ—Å—è—Ü</option>
              <option value="quarter">–ö–≤–∞—Ä—Ç–∞–ª</option>
              <option value="year">–ì–æ–¥</option>
            </select>
          </div>

          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">–†–µ–π—Ç–∏–Ω–≥</label>
            <select
              value={filter.rating || ''}
              onChange={(e) => handleFilterChange({ rating: e.target.value ? Number(e.target.value) : undefined })}
              className="border border-gray-300 rounded-md px-3 py-2 text-sm focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
            >
              <option value="">–í—Å–µ —Ä–µ–π—Ç–∏–Ω–≥–∏</option>
              <option value="5">5 –∑–≤–µ–∑–¥</option>
              <option value="4">4 –∑–≤–µ–∑–¥—ã</option>
              <option value="3">3 –∑–≤–µ–∑–¥—ã</option>
              <option value="2">2 –∑–≤–µ–∑–¥—ã</option>
              <option value="1">1 –∑–≤–µ–∑–¥–∞</option>
            </select>
          </div>

          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">–î–∞—Ç–∞ —Å</label>
            <input
              type="date"
              value={filter.dateFrom || ''}
              onChange={(e) => handleFilterChange({ dateFrom: e.target.value || undefined })}
              className="border border-gray-300 rounded-md px-3 py-2 text-sm focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
            />
          </div>

          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">–î–∞—Ç–∞ –¥–æ</label>
            <input
              type="date"
              value={filter.dateTo || ''}
              onChange={(e) => handleFilterChange({ dateTo: e.target.value || undefined })}
              className="border border-gray-300 rounded-md px-3 py-2 text-sm focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
            />
          </div>
        </div>
      </div>

      {/* –¢–∞–±—ã */}
      <div className="mb-6">
        <div className="border-b border-gray-200">
          <nav className="-mb-px flex space-x-8">
            <button
              onClick={() => setActiveTab('analytics')}
              className={`py-2 px-1 border-b-2 font-medium text-sm ${
                activeTab === 'analytics'
                  ? 'border-blue-500 text-blue-600'
                  : 'border-transparent text-gray-500 hover:text-gray-700 hover:border-gray-300'
              }`}
            >
              –ê–Ω–∞–ª–∏—Ç–∏–∫–∞
            </button>
            <button
              onClick={() => setActiveTab('reviews')}
              className={`py-2 px-1 border-b-2 font-medium text-sm ${
                activeTab === 'reviews'
                  ? 'border-blue-500 text-blue-600'
                  : 'border-transparent text-gray-500 hover:text-gray-700 hover:border-gray-300'
              }`}
            >
              –û—Ç–∑—ã–≤—ã
            </button>
            <button
              onClick={() => setActiveTab('add-review')}
              className={`py-2 px-1 border-b-2 font-medium text-sm ${
                activeTab === 'add-review'
                  ? 'border-blue-500 text-blue-600'
                  : 'border-transparent text-gray-500 hover:text-gray-700 hover:border-gray-300'
              }`}
            >
              –î–æ–±–∞–≤–∏—Ç—å –æ—Ç–∑—ã–≤
            </button>
          </nav>
        </div>
      </div>

      {/* –ö–æ–Ω—Ç–µ–Ω—Ç */}
      {activeTab === 'analytics' && renderAnalytics()}
      {activeTab === 'reviews' && renderReviews()}
      {activeTab === 'add-review' && (
        <ReviewForm 
          onSubmit={() => {
            // –ü–µ—Ä–µ–∫–ª—é—á–∞–µ–º—Å—è –Ω–∞ –≤–∫–ª–∞–¥–∫—É —Å –æ—Ç–∑—ã–≤–∞–º–∏ –ø–æ—Å–ª–µ —É—Å–ø–µ—à–Ω–æ–≥–æ –¥–æ–±–∞–≤–ª–µ–Ω–∏—è
            setActiveTab('reviews');
            // –û–±–Ω–æ–≤–ª—è–µ–º –¥–∞–Ω–Ω—ã–µ
            updateReviewsFilter(filter);
          }}
        />
      )}
    </div>
  );
};

export default Loyalty;
